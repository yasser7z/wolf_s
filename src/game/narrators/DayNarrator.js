class DayNarrator {
  static random(pool) {
    const jokes = JOKE_POOLS[pool];
    if (!jokes || jokes.length === 0) return '';
    return jokes[Math.floor(Math.random() * jokes.length)];
  }

  static dayStart(aliveCount) {
    const line = DayNarrator.random('dayStart');
    const list = '👥 **الأحياء:** ' + '\n_يمكن أحدهم ليس كما يبدو..._';
    return `${line}\n\n━━━━━━━━━━━━━━━━\n${list}\n━━━━━━━━━━━━━━━━\n\n🗣️ _ناقشوا واقنعوا بعضكم!_`;
  }

  static voteStart() {
    return DayNarrator.random('voteStart');
  }

  static voteResult(eliminated, tie) {
    if (tie || !eliminated) {
      return `${DayNarrator.random('voteResult')}\n\n⚖️ **تعادل! لا أحد سيتم إعدامه.**`;
    }
    return `${DayNarrator.random('voteResult')}\n\n⚖️ **تم إعدام <@${eliminated.id}>!**`;
  }

  static noDeath() {
    return DayNarrator.random('noDeath');
  }

  static discussion() {
    return DayNarrator.random('discussion');
  }
}

module.exports = DayNarrator;
